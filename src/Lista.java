
public class Lista {
	
	
	private String ime;
	private int broj;
	
	
	
	Lista () {
		
	}
	
	
	public Lista (String ime, int broj) {
		
		this.ime = ime;
		this.broj = broj;
		
		
	}


	public String getIme() {
		return ime;
	}


	public void setIme(String ime) {
		this.ime = ime;
	}


	public int getBroj() {
		return broj;
	}


	public void setBroj(int broj) {
		this.broj = broj;
	}
	

}


